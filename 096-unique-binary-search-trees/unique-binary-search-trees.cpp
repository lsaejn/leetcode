class Solution {
public:
    //只是问数量，并没有让求出来哦！
    //这个让你推导卡兰塔数列
    //http://www.cnblogs.com/grandyang/p/4299608.html
    int numTrees(int n) {
        vector<int> dp(n + 1, 0);
        dp[0] = 1;
        dp[1] = 1;
        for (int i = 2; i <= n; ++i) 
            for (int j = 0; j < i; ++j)
                dp[i] += dp[j] * dp[i - j - 1];
        
        return dp[n]; 
    }
};

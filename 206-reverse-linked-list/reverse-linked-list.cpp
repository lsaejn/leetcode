/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
//1->2->3
//null
 
class Solution {
public:
    ListNode* reverseList(ListNode* head) {
        if(!(head&&head->next))
           return head;
        ListNode* newHead=nullptr;
        while(head)
        {
            ListNode* temp=head;
            head=head->next;
            temp->next=newHead;
            newHead=temp;
        }
           return newHead;
        
    }
};
